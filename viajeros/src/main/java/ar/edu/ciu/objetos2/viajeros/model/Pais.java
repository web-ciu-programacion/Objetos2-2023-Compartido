package ar.edu.ciu.objetos2.viajeros.model;

public final class Pais {

	// atributos
	private String nombre;

	// constructor
	public Pais(String nombre) {
		super();
		this.nombre = nombre;
	}

	// gets y sets
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
